public class Ext {

    private int a;
    private double b;

    public Ext(double b, int a) {
        this.b = b;
        this.a = a;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }
}
